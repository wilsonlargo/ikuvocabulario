function VernacularAZ(a, b) {
    // Use toUpperCase() to ignore character casing
    const bandA = a.lx.toUpperCase();
    const bandB = b.lx.toUpperCase();

    let comparison = 0;
    if (bandA > bandB) {
        comparison = 1;
    } else if (bandA < bandB) {
        comparison = -1;
    }
    return comparison * OrderAZ;
}

